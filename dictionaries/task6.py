"""
Дан текст: в первой строке записано количество строк в тексте, а затем сами строки.
Выведите все слова, встречающиеся в тексте, по одному на каждую строку. Слова должны быть отсортированы
по убыванию их количества появления в тексте, а при одинаковой частоте появления —
в лексикографическом порядке.

Указание. После того, как вы создадите словарь всех слов, вам захочется отсортировать его
по частоте встречаемости слова. Желаемого можно добиться, если создать список, элементами
которого будут кортежи из двух элементов: частота встречаемости слова и само слово.
Например, [(2, 'hi'), (1, 'what'), (3, 'is')].
Тогда стандартная сортировка будет сортировать список кортежей,
при этом кортежи сравниваются по первому элементу, а если они равны — то по второму.
"""

n = int(input())
d = dict()

for i in range(n):
    sl = input().split()
    for j in sl:
        if j in d.keys():
            d[j] += 1
        else:
            d[j] = 1

l = []
for i in d.keys():
    l.append((d[i], i))
l = sorted(l)[::-1]
for i in l:
    print(i[0], i[1])
